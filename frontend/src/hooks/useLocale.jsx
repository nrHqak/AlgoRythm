const LocaleContext = createContext(null);

function getStoredLocale() {
  if (typeof window === "undefined") {
    return "en";
  }
  return window.localStorage.getItem("algorythm_locale") || "en";
}

function resolveValue(locale, key) {
  return key.split(".").reduce((accumulator, part) => accumulator?.[part], dictionaries[locale]);
}

export function LocaleProvider({ children }) {
  const [locale, setLocale] = useState(getStoredLocale());

  useEffect(() => {
    if (typeof window !== "undefined") {
      window.localStorage.setItem("algorythm_locale", locale);
      document.documentElement.lang = locale;
    }
  }, [locale]);

  const value = useMemo(
    () => ({
      locale,
      setLocale,
      t: (key) => resolveValue(locale, key) || key,
    }),
    [locale]
  );

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useLocale() {
  const context = useContext(LocaleContext);
  if (!context) {
    throw new Error("useLocale must be used within LocaleProvider.");
  }
  return context;
}
